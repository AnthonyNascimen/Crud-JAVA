package Model;

public class produto {
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getValor() {
        return valor;
    }
    public void setValor(String valor) {
        this.valor = valor;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    private int id;
    private String valor;
    private String nome;
}
